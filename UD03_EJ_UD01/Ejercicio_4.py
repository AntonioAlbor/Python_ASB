"""Escriba un programa que lea dos números, calcule y muestre el valor de sus suma, resta,
producto y división."""

num1 = float(input("Introduce el primer número: "))
num2 = float(input("Introduce el segundo número: "))
print("Suma:", num1 + num2,
      "Resta:", num1 - num2,
      "Producto:", num1 * num2,
      "División:", num1 / num2)
