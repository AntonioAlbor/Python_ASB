# Leer dos cadenas y concatenarlas manualmente sin usar el operador + en una sola operación (concatenar carácter a carácter con un ciclo)

cadena1 = input("Ingresa una cadena: ")
cadena2 = input("Ingresa otra cadena: ")

cadenas_unir = (cadena1, cadena2)

cadena_final = " ".join(cadenas_unir)

print(cadena_final)
